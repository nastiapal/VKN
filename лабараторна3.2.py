#Так як у прямокутника праралельні сторони рівні,то АВ=СD=a,та AC=BD=b;
import sys
a=int(sys.argv[1])
b=int(sys.argv[2])
#Знайдемо координати т.B;
xb=a//2
yb=b//2
print ("т.B=(",xb,";",yb,")")
#Знайдемо координати т.С;
xc=(0-(a//2))
yc=(0-(b//2))
print ("т.C=(",xc,";",yc,")")
#Знайдемо координати т.А
xa=(0-(a//2))
ya=b//2
print ("т.A=(",xa,";",ya,")")
#Знайдемо координати т.D;
xd=a//2
yd=(0-(b//2))
print("т.D=(",xd,";",yd,")")